// Boolean é um tipo primitivo em JavaScript que pode ter apenas dois valores: `true` (verdadeiro) ou `false` (falso) ³. Os valores booleanos são usados principalmente em comparações e condições.

// Para declarar uma variável e atribuir um valor booleano a ela, você pode usar a palavra-chave `var`, `let` ou `const`, seguida pelo nome da variável, o sinal de igual (`=`) e o valor booleano:

// ```javascript
// let x = true;
// let y = false;
// ```

// Você também pode usar a função global `Boolean` para converter um valor de outro tipo em booleano ⁴. Por exemplo, para converter uma string em um valor booleano, você pode fazer o seguinte:

// ```javascript
// const x = 'uma string';
// const y = Boolean(x); // true
// ```

// Além disso, os operadores de comparação em JavaScript (`==`, `!=`, `===`, `!==`, `<`, `>`, `<=` e `>=`) retornam valores booleanos quando usados em expressões:

// ```javascript
// const x = 1;
// const y = 2;
// const z = x < y; // true
// ```

